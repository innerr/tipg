CREATE TABLE IF NOT EXISTS lineitem2 (
	L_ORDERKEY     INTEGER NOT NULL,
	L_PARTKEY      INTEGER NOT NULL,
	L_SUPPKEY      INTEGER NOT NULL,
	L_LINENUMBER   INTEGER NOT NULL,
	L_QUANTITY     FLOAT NOT NULL,
	L_EXTENDEDPRICE   FLOAT NOT NULL,
	L_DISCOUNT     FLOAT NOT NULL,
	L_TAX          FLOAT NOT NULL,
	L_RETURNFLAG   CHAR(1) NOT NULL,
	L_LINESTATUS   CHAR(1) NOT NULL,
	L_SHIPDATE     DATE NOT NULL,
	L_COMMITDATE   DATE NOT NULL,
	L_RECEIPTDATE  DATE NOT NULL,
	L_SHIPINSTRUCT CHAR(25) NOT NULL,
	L_SHIPMODE     CHAR(10) NOT NULL,
	L_COMMENT      VARCHAR(44) NOT NULL,
	L_ORDERKEY2    INTEGER NOT NULL,
	L_PARTKEY2     INTEGER NOT NULL,
	L_SUPPKEY2     INTEGER NOT NULL,
	L_LINENUMBER2  INTEGER NOT NULL,
	L_QUANTITY2    FLOAT NOT NULL,
	L_EXTENDEDPRICE2  FLOAT NOT NULL,
	L_DISCOUNT2    FLOAT NOT NULL,
	L_TAX2         FLOAT NOT NULL,
	L_RETURNFLAG2  CHAR(1) NOT NULL,
	L_LINESTATUS2  CHAR(1) NOT NULL,
	L_SHIPDATE2    DATE NOT NULL,
	L_COMMITDATE2  DATE NOT NULL,
	L_RECEIPTDATE2 DATE NOT NULL,
	L_SHIPINSTRUCT2 CHAR(25) NOT NULL,
	L_SHIPMODE2     CHAR(10) NOT NULL,
	L_COMMENT2      VARCHAR(44) NOT NULL,
	PRIMARY KEY (L_ORDERKEY,L_LINENUMBER),
	CONSTRAINT FOREIGN KEY LINEITEM_FK1 (L_ORDERKEY)  references orders(O_ORDERKEY),
	CONSTRAINT FOREIGN KEY LINEITEM_FK2 (L_PARTKEY,L_SUPPKEY) references partsupp(PS_PARTKEY, PS_SUPPKEY));
